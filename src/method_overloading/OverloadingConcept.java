package method_overloading;

/*In Java, two or more methods may have the same name if they differ in 
 * parameters (different number of parameters, different types of parameters, or both). 
 * These methods are called overloaded methods and this feature is called method overloading. 
 * For example:
void func() { ... }
void func(int a) { ... }
float func(double a) { ... }
float func(int a, float b) { ... } */